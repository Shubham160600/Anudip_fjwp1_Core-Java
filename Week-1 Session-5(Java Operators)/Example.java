package shubham;

public class Example {
public static void main(String[] args) {
// Create variables
int a = 3;
int var;
System.out.println("a = " + a);
// assign value using =
var = a;
System.out.println("var using = " + var);
// assign value using +=
var += a;
System.out.println("var using += " + var);
var -= a;
System.out.println ( "var using -= " + var);
// assign value using *=
var *= a;
System.out.println("var using *= " + var);
// assign value using /=
var /= a ;
System.out.println("var using /= " + var);
// assign value using %=
var %= a;
System.out.println("var using %= " + var);
}
}
