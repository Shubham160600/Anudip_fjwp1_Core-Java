package shubham;

public class BreakExample {
    public static void main(String[] args) {
        public class BreakExample {
            public static void main(String[] args) {
                // Initializing a while loop with a counter 'a'
                int a = 1;

                // While loop to iterate from 1 to 10
                while (a <= 10) {
                    // Checking if the current value of 'a' is 5
                    if (a == 5) {
                        // Using the break statement to exit the loop when 'a' is 5
                        a++;
                        break; // It will break the while loop
                    }

                    // Printing the current value of 'a'
                    System.out.println(a);

                    // Incrementing the counter 'a'
                    a++;
                }
            }
        }

        // initiating while loop
        int a = 1;
        while (a <= 10) {
            if (a == 5) {
                // using break statement
                a++;
                break;// it will break the while loop
            }
            System.out.println(a);
            a++;
        }
    }
}
